package com.ltvs.pojo;

/**
 * 定时器 pojo
 * 
 * @author admin
 *
 */
public class LtvsQuartz {

	//定时任务id
	private Integer qtid;
	//定时任务 名称
	private String qtname;
	//定时任务所在类
	private String qtclass;
	//执行的时间
	private String qttime;
	//任务状态
	private String qtstatus;
	//触发器名称
	private String qtcron;
	//任务信息
	private String qtinfo;

	public String getQtinfo() {
		return qtinfo;
	}

	public void setQtinfo(String qtinfo) {
		this.qtinfo = qtinfo;
	}

	public Integer getQtid() {
		return qtid;
	}

	public void setQtid(Integer qtid) {
		this.qtid = qtid;
	}

	public String getQtname() {
		return qtname;
	}

	public void setQtname(String qtname) {
		this.qtname = qtname;
	}

	public String getQtclass() {
		return qtclass;
	}

	public void setQtclass(String qtclass) {
		this.qtclass = qtclass;
	}

	public String getQttime() {
		return qttime;
	}

	public void setQttime(String qttime) {
		this.qttime = qttime;
	}

	public String getQtstatus() {
		return qtstatus;
	}

	public void setQtstatus(String qtstatus) {
		this.qtstatus = qtstatus;
	}

	public String getQtcron() {
		return qtcron;
	}

	public void setQtcron(String qtcron) {
		this.qtcron = qtcron;
	}

}
